/**
 * Get supported player counts for a given format
 */
export function getMeadSupportedCounts(format: MeadFormatKey): number[] {
  return Object.entries(MEAD_TABLE)
    .filter(([, tables]) => tables[format] !== undefined)
    .map(([count]) => parseInt(count))
    .sort((a, b) => a - b);
}

/**
 * Get the Mead table for a given player count and format.
 * Returns null if not supported.
 */
export function getMeadTable(playerCount: number, format: MeadFormatKey): MeadTable | null {
  return MEAD_TABLE[playerCount]?.[format] ?? null;
}
