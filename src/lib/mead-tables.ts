/**
 * Mead Draw Rotation Tables
 *
 * Standard published tables for multi-round lawn bowling rotation.
 * Each table entry maps player indices to rink/team/position assignments per round.
 * Format: [rink][team][position_index] = player_number (1-based)
 *
 * For fours: 4 positions per team, 2 teams per rink
 * For triples: 3 positions per team, 2 teams per rink
 * For pairs: 2 positions per team, 2 teams per rink
 *
 * Tables indexed by player count and format key.
 */

export type MeadFormatKey = "fours" | "triples" | "pairs";

// Each round is an array of rinks. Each rink is [team1[], team2[]]
// Player numbers are 1-based indices that map to shuffled player array
type RinkAssignment = [number[], number[]];
type Round = RinkAssignment[];
type MeadTable = Round[];

